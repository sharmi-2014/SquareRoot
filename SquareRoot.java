package javachallenge;

public class SquareRoot {
public static void main(String[] args) {
	
	int num=4;
	for( int i=0;i<=num/2;i++) {
	if(i*i==num) {
		System.out.println(i+ "is the square root i");
	}
	}
	int num1=8;
	for(int j=0;j<=num1/2;j++) {
		if(j*j==num1/2) {
			System.out.println(j+ " is the square root of j");
		}
	}
		
	}
}